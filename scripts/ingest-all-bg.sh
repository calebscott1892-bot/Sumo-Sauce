#!/bin/bash
# Live-fetches all basho from SumoDB sequentially.
# Run: bash scripts/ingest-all-bg.sh
set -euo pipefail
cd "$(dirname "$0")/.."

export INGEST_MODE=live
export INGEST_RATE_LIMIT_MS=500

BASHO_LIST=(
  200001 200003 200005 200007 200009 200011
  200101 200103 200105 200107 200109 200111
  200201 200203 200205 200207 200209 200211
  200301 200303 200305 200307 200309 200311
  200401 200403 200405 200407 200409 200411
  200501 200503 200505 200507 200509 200511
  200601 200603 200605 200607 200609 200611
  200701 200703 200705 200707 200709 200711
  200801 200803 200805 200807 200809 200811
  200901 200903 200905 200907 200909 200911
  201001 201003 201005 201007 201009 201011
  201101 201105 201107 201109 201111
  201201 201203 201205 201207 201209 201211
  201301 201303 201305 201307 201309 201311
  201401 201403 201405 201407 201409 201411
  201501 201503 201505 201507 201509 201511
  201601 201603 201605 201607 201609 201611
  201701 201703 201705 201707 201709 201711
  201801 201803 201805 201807 201809 201811
  201901 201903 201905 201907 201909 201911
  202001 202003 202007 202009 202011
  202101 202103 202105 202107 202109 202111
  202201 202203 202205 202207 202209 202211
  202301 202303 202305 202307 202309 202311
  202401 202403
)

TOTAL=${#BASHO_LIST[@]}
echo "[$(date -u +%FT%TZ)] Starting ingestion of $TOTAL basho"
OK=0
FAIL=0

for basho in "${BASHO_LIST[@]}"; do
  echo -n "[$(date -u +%FT%TZ)] $basho ... "
  if node --experimental-strip-types scripts/ingest-range.mjs --basho "$basho" --mode live --force 2>/dev/null | grep -q '"status":"COMPLETE"'; then
    echo "OK"
    OK=$((OK + 1))
  else
    echo "FAIL"
    FAIL=$((FAIL + 1))
  fi
done

echo ""
echo "[$(date -u +%FT%TZ)] Done: $OK OK, $FAIL FAIL out of $TOTAL"
